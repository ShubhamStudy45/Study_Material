//data types :

//all data types in JS are inferred
//will be dynamically assigned to the variable by inspecting value at the time.

//Number
const num = 100;
console.log('num = ',num)
console.log('type of num = ',typeof num)

//number 
const num1 = 3.56
console.log('num1 = ',num1)
console.log('type of num1 = ',typeof num1)

//string 
const fname = 'shubham'
console.log('name = ',fname)
console.log('type of fname = ',typeof fname)

//string 
const address = `new hanuman nager,
behind v.m.v college,
Amravati`

console.log('address = ',address)
console.log('type of address = ',typeof address)

//object

const person = {
    fname : 'abc',
    lname: 'xyz',
    address : 'abcxyz',
    phone : '+918376664'
}

console.log('person = ',person)
console.log('type of person = ',typeof person)




